/*
JPA-26: Contar de 1 a 10

Um professor quer que o computador mostre automaticamente os números usados em uma chamada de
alunos.
O programa deve imprimir os números de 1 até 10 utilizando um laço for.
*/

#include <stdlib.h>
#include <stdio.h>

int main()
{
    printf("\n************************************************************");
    printf("\n* Aluno: DIEGO FAGUNDES - RA 0027084                       *");
    printf("\n* JPA-26: Contar de 1 a 10");
    printf("\n************************************************************\n\n");

    for (int i = 1; i <= 10; i++)
    {
        printf("%d\n", i);
    }
    

}